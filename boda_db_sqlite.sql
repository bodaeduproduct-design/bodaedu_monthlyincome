CREATE TABLE monthly_lesson_records (
  id                 INTEGER PRIMARY KEY AUTOINCREMENT,
  billing_month      TEXT NOT NULL,
  subscription_id    INTEGER NOT NULL,
  student_id         INTEGER NOT NULL,
  teacher_id         INTEGER NOT NULL,
