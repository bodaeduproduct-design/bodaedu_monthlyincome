CREATE TABLE commission_rate_history (
  id               INTEGER PRIMARY KEY AUTOINCREMENT,
  subscription_id  INTEGER NOT NULL,
  previous_rate    REAL NOT NULL,
  new_rate         REAL NOT NULL,
